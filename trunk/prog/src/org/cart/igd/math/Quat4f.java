package org.cart.igd.math;

/**
 * Quat4f.java
 *
 * General Function: Holds 4 Float values.
 */
public class Quat4f
{
	/* X-Axis value. */
	public float x;
	
	/* Y-Axis value. */
	public float y;
	
	/* Z-Axis value. */
	public float z;
	
	/* Extra value, sometimes used for Rotation transforms as the amount to rotate. */
	public float w;
}
