package org.cart.igd.discreet;

public class PositionData extends TrackData
{
    /** The x axis value */
    public float x;

    /** The y axis value */
    public float y;

    /** The z axis value */
    public float z;
    
    public String toString()
    {
    	return "x:"+x+"; y:"+y+"; z:"+z+";";
    }
}
