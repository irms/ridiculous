package org.cart.igd.math;

/**
 * Point2f.java
 *
 * General Function: A simple X and Y Float point.
 */
public class Point2f
{
	/* X-Axis Position. */
	public float x;
	
	/* Y-Axis Position. */
	public float y;

	/**
	 * Constructor
	 *
	 * General Function: Creates an instance of Point2f.
	 *
	 * @param x The x-axis position.
	 * @param y The y-axis position.
	 */
	public Point2f(float x, float y)
	{
		this.x = x;
		this.y = y;
	}
}